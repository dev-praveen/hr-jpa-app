create table regions (
  region_id int not null generated by default as identity,
  region_name varchar(25),
  primary key (region_id)
);
create table countries (
  country_id char(2),
  country_name varchar(40),
  region_id int not null,
  primary key (country_id),
  foreign key (region_id)
  references regions (region_id) on delete cascade on update cascade
);
create table locations (
  location_id int not null generated by default as identity,
  street_address varchar(40),
  postal_code varchar(12),
  city varchar(30) not null,
  state_province varchar(25),
  country_id char(2) not null,
  primary key (location_id),
  foreign key (country_id)
  references countries (country_id) on delete cascade on update cascade
);
create table departments (
  department_id int not null generated by default as identity,
  department_name varchar(30) not null,
  location_id int,
  primary key (department_id),
  foreign key (location_id)
  references locations (location_id) on delete cascade on update cascade
);
create table jobs (
  job_id int not null generated by default as identity,
  job_title varchar(35) not null,
  min_salary numeric(8, 2),
  max_salary numeric(8, 2),
  primary key (job_id)
);
create table employees (
  employee_id int not null generated by default as identity,
  first_name varchar(20),
  last_name varchar(25) not null,
  email varchar(100) not null,
  phone_number varchar(20),
  hire_date date not null,
  job_id int not null,
  salary numeric(8, 2) not null,
  manager_id int,
  department_id int,
  primary key (employee_id),
  foreign key (job_id)
  references jobs (job_id) on delete cascade on update cascade,
  foreign key (department_id)
  references departments (department_id) on delete cascade on update cascade,
  foreign key (manager_id)
  references employees (employee_id) on delete cascade on update cascade
);
create table dependents (
  dependent_id int not null generated by default as identity,
  first_name varchar(50) not null,
  last_name varchar(50) not null,
  relationship varchar(25) not null,
  employee_id int not null,
  primary key (dependent_id),
  foreign key (employee_id)
  references employees (employee_id) on delete cascade on update cascade
);


